func TestChangePos(t *testing.T) {
	cases := [][]int{
		{50, 50, 0},
		{50, -50, 0},
		{50, 51, 1},
		{50, -51, 99},
		{50, -68, 82},
		{99, 100, 99},
		{99, 500, 99},
		{99, 550, 49},
		{99, 1000, 99},
		{99, -100, 99},
		{99, -500, 99},
		{99, -550, 49},
		{99, -1000, 99},
		{0, -1, 99},
		{0, -100, 0},
		{0, -500, 0},
		{0, -1000, 0},
		{82, -30, 52},
		{55, -55, 0},
		{50, -100, 50},
		{77, -100, 77},
		{77, 750, 27},
		{77, -700, 77},
		{77, -750, 27},
		{1, -1, 0},
		{1, -2, 99},
		{1, -3, 98},
		{99, -99, 0},
		{0, -10, 90},
		{0, -150, 50},
		{49, -50, 99},
		{49, -450, 99},
		{49, -4050, 99},
		{49, -451, 98},
		{49, -449, 0},
		{49, -448, 1},
	}
	for _, c := range cases {
		want := c[2]
		got := ChangePos(c[0], c[1])
		if want != got {
			t.Errorf(`ChangePos(%v, %v) want %v, got %v`, c[0], c[1],
				want, got)
		}
	}
}

